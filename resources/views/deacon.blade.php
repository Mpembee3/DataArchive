                $(document).ready(function() {
    // Trigger modal via member name click
    $('.member-link').on('click', function(event) {
        event.preventDefault();  // This prevents the page from jumping to the top

        // Get the member ID from the data attribute
        var memberId = $(this).data('id');

        // Send AJAX request to fetch member details
        $.ajax({
            url: '/deacon/' + memberId,  // This route will handle fetching the member data
            method: 'GET',
            success: function(data) {
                // Update modal content with the fetched data
                $('#memberName').text(data.fname + ' ' + data.lname);
                $('#memberPhone').text(data.phone);
                $('#memberGender').text(data.gender);
                $('#memberEmail').text(data.email);
                $('#memberAddress').text(data.address);
                $('#memberMaritalStatus').text(data.marital_status);
                $('#memberSupervisor').text(data.name);
                
                // Show the modal
                $('#memberModal').modal('show');
            },
            error: function() {
                alert('Failed to fetch member details');
            }
        });
